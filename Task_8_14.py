# Главный метод
def main():

    print(" >> Метод нахождения индекса минимальной цифры в числе.")

    Num = int(input("Введите число: "))

    print("Пример 1:",Ordinal_Min_Index(Num,"Start"))    
    print("Пример 2:",Ordinal_Min_Index(Num,"End"))       

# -------------------------------------------------------------
if __name__ == "__main__": main()
